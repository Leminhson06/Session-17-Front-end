// Sử dụng hàm Math.abs()
let absValue1 = Math.abs(-5.5);
// Kết quả: 5.5
// Giải thích: Math.abs() trả về giá trị tuyệt đối của số truyền vào, do đó -5.5 trở thành 5.5
console.log(absValue1);

let absValue2 = Math.abs(10);
// Kết quả: 10
// Giải thích: Math.abs() trả về giá trị tuyệt đối của số truyền vào, do đó 10 vẫn là 10
console.log(absValue2);

// Sử dụng hàm Math.ceil()
